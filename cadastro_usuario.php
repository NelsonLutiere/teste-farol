Título: Criação de um Formulário de Cadastro de Usuário com HTML, CSS.

Objetivo: Desenvolver um formulário de cadastro de usuário utilizando HTML para a estrutura, CSS para o estilo e JavaScript para validação de senha.

Requisitos:

Utilizar HTML5 para criar a estrutura do formulário.
Implementar o método POST para o envio dos dados.
Criar um layout atraente com CSS, incluindo bordas e sombras.
O formulário deve conter os seguintes campos:
Nome completo
E-mail
Senha
Confirmação de senha
Data de nascimento
Gênero (select)
Termos de uso (checkbox)
Adicionar um botão de envio.